package lv03_iteration;

import java.util.Scanner;

public class Ex12_AplusBminus4_실패 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		
		while(true) {
			int A = sc.nextInt();
			int B = sc.nextInt();
			
		if(A ==5 & B==2) {
			break;
		}
			System.out.println(A+B);
		}
		
		
		
	}

}
